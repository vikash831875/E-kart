const sampleProducts = productDefinitions.map((product) => {
  const categorySet = categoryImages[product.category] || categoryImages.Smartphones;
  const primaryImage = categorySet[product.imageIndex % categorySet.length];
  const imageSet = [
    primaryImage,
    categorySet[(product.imageIndex + 1) % categorySet.length],
    categorySet[(product.imageIndex + 2) % categorySet.length],
  ];

  return {
    productName: product.productName,
    shortDescription: product.shortDescription,
    fullDescription: product.fullDescription,
    productDesc: product.shortDescription,
    price: product.price,
    productPrice: product.price,
    originalPrice: product.originalPrice,
    discountPercentage: product.discountPercentage,
    stock: product.stock,
    thumbnail: primaryImage,
    images: imageSet,
    productImg: [
      {
        url: primaryImage,
        public_id: product.public_id,
      },
    ],
    rating: product.rating,
    numReviews: product.numReviews,
    specifications: product.specs.map(([label, value]) => ({ label, value })),
    featured: product.featured,
    category: product.category,
    brand: product.brand,
  };
});

const seedDatabase = async () => {
  try {
    await connectDB();

    const existingNames = await Product.find({
      productName: { $in: sampleProducts.map((p) => p.productName) },
    }).distinct("productName");

    const productsToInsert = sampleProducts.filter(
      (product) => !existingNames.includes(product.productName)
    );

    if (productsToInsert.length === 0) {
      console.log("All sample products already exist in the database. No new products inserted.");
      process.exit(0);
    }

    await Product.insertMany(productsToInsert);
    console.log(`Inserted ${productsToInsert.length} new products into MongoDB.`);
    process.exit(0);
  } catch (error) {
    console.error("Failed to seed products:", error);
    process.exit(1);
  }
};

await seedDatabase();
